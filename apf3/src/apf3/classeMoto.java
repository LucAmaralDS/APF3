package apf3;

public class classeMoto {
	private int id_moto;
	private String tipo_cambio_moto;
	private String cor_moto;
	private float motor_moto;
	
	
	public int getId_moto() {
		return id_moto;
	}
	
	public String getTipo_cambio_moto() {
		return tipo_cambio_moto;
	}
	
	public String getCor_moto() {
		return cor_moto;
	}
	
	public float getMotor_moto() {
		return motor_moto;
	}

	public void setId_moto(int id_moto) {
		this.id_moto = id_moto;
	}
	
	public void setTipo_cambio_moto(String tipo_cambio_moto) {
		this.tipo_cambio_moto = tipo_cambio_moto;
	}
	
	public void setCor_moto(String cor_moto) {
		this.cor_moto = cor_moto;
	}
	
	public void setMotor_moto(float motor_moto) {
		this.motor_moto = motor_moto;
	}
		
	
}
